#include <unistd.h>

void	ft_putnbr(int n)
{
	char	list[11];
	int		i;

	i = 0;
	if (n == 0)
		write(1, "0", 1);
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	while (n > 0)
	{
		list[i++] = (n % 10) + '0';
		n /= 10;
	}
	while (i > 0)
		write(1, &list[--i], 1);
}
